export const formatReportListInfo = (reports, type) => {
    let reportInfo = "";
    for (const r of reports) {
        reportInfo += type + " report 0x" + hex8(r.reportId) + "\n";
        let bitOffset = 0;
        for (const item of r.items) {
            reportInfo +=
                "  " + reportSizeAndCountAsString(item, bitOffset) + "\n";
            reportInfo += "    " + bitFieldAsString(item) + "\n";
            if (hasUsages(item))
                reportInfo += "    " + usagesAsString(item) + "\n";
            reportInfo += "    " + logicalBoundsAsString(item) + "\n";
            if (hasPhysicalBounds(item))
                reportInfo += "    " + physicalBoundsAsString(item) + "\n";
            if (hasUnitDefinition(item))
                reportInfo += "    " + unitsAsString(item) + "\n";
            bitOffset += item.reportSize * item.reportCount;
        }
    }
    return reportInfo;
};

export const hex8 = (value) => {
    return ("00" + value.toString(16)).substr(-2).toUpperCase();
};
export const hex16 = (value) => {
    return ("0000" + value.toString(16)).substr(-4).toUpperCase();
};

const bitFieldAsString = (item) => {
    let bits = [];
    bits.push(item.isConstant ? "Cnst" : "Data");
    bits.push(item.isArray ? "Ary" : "Var");
    bits.push(item.isAbsolute ? "Abs" : "Rel");
    if (item.wrap) bits.push("Wrap");
    if (!item.isLinear) bits.push("NLin");
    if (!item.hasPreferredState) bits.push("NPrf");
    if (item.hasNull) bits.push("Null");
    if (item.isVolatile) bits.push("Vol");
    if (item.isBufferedBytes) bits.push("Buf");
    return bits.join(",");
};

const hasUsages = (item) => {
    return item.isRange || item.usages.length > 0;
};

const hasPhysicalBounds = (item) => {
    return (
        item.physicalMinimum != 0 ||
        item.physicalMaximum != item.physicalMinimum
    );
    4;
};

const usagesAsString = (item) => {
    if (item.isRange) {
        if (item.usageMinimum == item.usageMaximum)
            return "Usage: " + usageName(item.usageMinimum);
        return (
            "Usages: " +
            usageName(item.usageMinimum) +
            " to " +
            usageName(item.usageMaximum)
        );
    }

    let usageStrings = [];
    for (const usage of item.usages) usageStrings.push(usageName(usage));
    if (usageStrings.length == 1) return "Usage: " + usageStrings[0];
    return "Usages: " + usageStrings.join(", ");
};

const logicalBoundsAsString = (item) => {
    return (
        "Logical bounds: " + item.logicalMinimum + " to " + item.logicalMaximum
    );
};

// Returns the physical bounds of |item| as a human-readable string.
const physicalBoundsAsString = (item) => {
    return (
        "Physical bounds: " +
        item.physicalMinimum +
        " to " +
        item.physicalMaximum
    );
};

const reportSizeAndCountAsString = (item, startBit) => {
    const bitWidth = item.reportCount * item.reportSize;
    if (bitWidth == 1) return "1 bit (bit " + startBit + ")";

    const endBit = startBit + bitWidth - 1;
    if (item.reportCount == 1)
        return (
            item.reportSize + " bits (bits " + startBit + " to " + endBit + ")"
        );

    return (
        item.reportCount +
        " values * " +
        item.reportSize +
        " bits (bits " +
        startBit +
        " to " +
        endBit +
        ")"
    );
};

const hasUnitDefinition = (item) => {
    return (
        item.unitFactorLengthExponent != 0 ||
        item.unitFactorMassExponent != 0 ||
        item.unitFactorTimeExponent != 0 ||
        item.unitFactorTemperatureExponent != 0 ||
        item.unitFactorCurrentExponent != 0 ||
        item.unitFactorLuminousIntensityExponent != 0 ||
        item.unitExponent != 0 ||
        item.unitSystem != "none"
    );
};

const unitsAsString = (item) => {
    let lengthName;
    let massName;
    let timeName;
    let temperatureName;
    let currentName;
    let luminousIntensityName;
    if (item.unitSystem == "si-linear") {
        lengthName = "cm"; // Centimeter
        massName = "g"; // Gram
        timeName = "s"; // Seconds
        temperatureName = "K"; // Kelvin
        currentName = "A"; // Ampere
        luminousIntensityName = "cd"; // Candela
    } else if (item.unitSystem == "si-rotation") {
        lengthName = "rad"; // Radians
        massName = "g"; // Gram
        timeName = "s"; // Seconds
        temperatureName = "K"; // Kelvin
        currentName = "A"; // Ampere
        luminousIntensityName = "cd"; // Candela
    } else if (item.unitSystem == "english-linear") {
        lengthName = "in"; // Inch
        massName = "slug"; // Slug
        timeName = "s"; // Seconds
        temperatureName = "°F"; // Fahrenheit
        currentName = "A"; // Ampere
        luminousIntensityName = "cd"; // Candela
    } else if (item.unitSystem == "english-rotation") {
        lengthName = "deg"; // Degrees
        massName = "slug"; // Slug
        timeName = "s"; // Seconds
        temperatureName = "°F"; // Fahrenheit
        currentName = "A"; // Ampere
        luminousIntensityName = "cd"; // Candela
    } else {
        lengthName = "length";
        massName = "mass";
        timeName = "time";
        temperatureName = "temperature";
        currentName = "current";
        luminousIntensityName = "luminous-intensity";
    }

    let numeratorFactors = [];
    let denominatorFactors = [];
    addUnitFactor(
        numeratorFactors,
        denominatorFactors,
        lengthName,
        item.unitFactorLengthExponent,
    );
    addUnitFactor(
        numeratorFactors,
        denominatorFactors,
        massName,
        item.unitFactorMassExponent,
    );
    addUnitFactor(
        numeratorFactors,
        denominatorFactors,
        timeName,
        item.unitFactorTimeExponent,
    );
    addUnitFactor(
        numeratorFactors,
        denominatorFactors,
        temperatureName,
        item.unitFactorTemperatureExponent,
    );
    addUnitFactor(
        numeratorFactors,
        denominatorFactors,
        currentName,
        item.unitFactorCurrentExponent,
    );
    addUnitFactor(
        numeratorFactors,
        denominatorFactors,
        luminousIntensityName,
        item.unitFactorLuminousIntensityExponent,
    );

    const exponent =
        item.unitExponent == 0 ? "" : "10^" + item.unitExponent + "*";
    const numerator =
        numeratorFactors.length > 0 ? numeratorFactors.join("*") : "1";
    if (denominatorFactors.length == 0) return "Units: " + exponent + numerator;

    const denominator = denominatorFactors.join("*");
    return "Units: " + exponent + numerator + "/" + denominator;
};

const addUnitFactor = (
    numerator,
    denominator,
    unitFactorName,
    unitFactorExponent,
) => {
    if (unitFactorExponent == 0) return;

    const absoluteExponent = Math.abs(unitFactorExponent);
    const exponent = absoluteExponent == 1 ? "" : "^" + absoluteExponent;
    if (unitFactorExponent > 0) numerator.push(unitFactorName + exponent);
    else denominator.push(unitFactorName + exponent);
};

export const printDeviceInfo = (selectedDevice) => {
	if (selectedDevice == null) {
		return;
	}

	let deviceInfo =
		"productName: " +
		selectedDevice.productName +
		"\n" +
		"vendorId:    0x" +
		hex16(selectedDevice.vendorId) +
		" (" +
		selectedDevice.vendorId +
		")\n" +
		"productId:   0x" +
		hex16(selectedDevice.productId) +
		" (" +
		selectedDevice.productId +
		")\n" +
		"opened:      " +
		(selectedDevice.opened ? "true" : "false") +
		"\n";

	if (selectedDevice.collections.length == 0) {
		deviceInfo += "collections: None";
	} else {
		for (let i = 0; i < selectedDevice.collections.length; ++i) {
			const c = selectedDevice.collections[i];
			let inputReports = [];
			let outputReports = [];
			let featureReports = [];
			for (const r of c.inputReports)
				inputReports.push("0x" + hex8(r.reportId));
			for (const r of c.outputReports)
				outputReports.push("0x" + hex8(r.reportId));
			for (const r of c.featureReports)
				featureReports.push("0x" + hex8(r.reportId));

			deviceInfo += "collections[" + i + "]\n";
			deviceInfo +=
				"  Usage " +
				hex16(c.usagePage) +
				":" +
				hex16(c.usage) +
				"\n";
			if (inputReports.length > 0)
				deviceInfo +=
					"  Input reports: " + inputReports.join(", ") + "\n";
			if (outputReports.length > 0)
				deviceInfo +=
					"  Output reports: " + outputReports.join(", ") + "\n";
			if (featureReports.length > 0)
				deviceInfo +=
					"  Feature reports: " +
					featureReports.join(", ") +
					"\n";
		}

		for (let i = 0; i < selectedDevice.collections.length; ++i) {
			const c = selectedDevice.collections[i];
			deviceInfo += formatReportListInfo(c.inputReports, "Input");
			deviceInfo += formatReportListInfo(c.outputReports, "Output");
			deviceInfo += formatReportListInfo(c.featureReports, "Feature");
		}
	}

	console.info(deviceInfo);
};

